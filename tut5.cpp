#include <iostream>
using namespace std;
int main()
{
    int num1,num2;
    cout<<"Enter the value of number 1:\n";    //This is called insertion operator
    cin>>num1;                                 //This is extraction operator.
    cout<<"Enter the value of number 2:\n";    //This is called insertion operator
    cin>>num2;                                 //This is extraction operator.
    cout << "This is the sum "<<num1+num2;
    return 0;
}
